package array;

//practice

public class Array {
	public static void main(String []args) {
		
		int []a = new int [5];
		for (int aa :a) {
			System.out.println(aa);
		}
		
		boolean []b = new boolean[5];
		for (boolean bb:b) {
			System.out.println(bb);
		}
		
		String []c = new String [5];
		for (String cc:c) {
			System.out.println(cc);
		}
		}
		
		
	}

